function searchProject(listaDeProjetos, nomeDoProjeto){
    for (let i = 0; i < listaDeProjetos.length; i++) {
        if (listaDeProjetos[i].name === nomeDoProjeto) {
            return listaDeProjetos, position = [i]
            break;
        }
    }
};

